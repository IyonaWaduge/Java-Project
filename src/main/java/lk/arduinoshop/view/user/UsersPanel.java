    private void saveButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveButtonActionPerformed
//        String error = "Are you want sure to change password?";
//        JTextArea outputArea = new JTextArea(12,10);
//        outputArea.setText(error);
//        JOptionPane.showMessageDialog(null, error,"Confirmation Message", JOptionPane.QUESTION_MESSAGE);// TODO add your handling code here:
        // ----- Check password Correct
        String userName = useNameTextField.getText();
        String password = new String(passwordField.getPassword());
        String rePassword = new String(rePasswordField.getPassword());

        for (UserDTO userDTO : allUsers) {
            if (userDTO.getUserName().equals(userName)) {
                String error = "User name already exist ! Please use another user Name.";
                JOptionPane.showMessageDialog(this, error, "User Exist", JOptionPane.ERROR_MESSAGE);
                return;
            }
        }

        if (!password.equals(rePassword)) {
            String error = "Re-Enter password not matched.";
            JOptionPane.showMessageDialog(this, error, "Password Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        if(selectedPrivilegesLiast.getModel().getSize() < 1) {
            String error = "You need to select at least 1 privilege for the user.";
            JOptionPane.showMessageDialog(this, error, "Privilege Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        // ------- Create userDto
        UserDTO userDTO = new UserDTO();
        userDTO.setUserName(useNameTextField.getText());
        userDTO.setPassword(HashUtil.convertToHash(password));
        userDTO.setUserStatus("NEW");

        // -------- Add privilage to userDTO
        List<UserPrivilegeDTO> userPrivilegeList = new ArrayList<>();

        int maxId = UserController.maxID();
        for (int i = 0; i < selectedPrivilegesLiast.getModel().getSize(); i++) {
            String item = selectedPrivilegesLiast.getModel().getElementAt(i);
            UserPrivilegeDTO userPrivilegeDTO = new UserPrivilegeDTO();
            userPrivilegeDTO.setPrivilege(item);
            userPrivilegeDTO.setId(maxId);
            userPrivilegeList.add(userPrivilegeDTO);

            userDTO.setUserPrivilage(userPrivilegeList);
        }

        // ------ user and privilege
        boolean userAdded = UserController.addUserWithPrivilege(userDTO);

        // ---- refresh loadUsersAndPrivileges() 
        loadUsersAndPrivileges();

        // ----- clear fields 
        clearPanelData();

    }//GEN-LAST:event_saveButtonActionPerformed

