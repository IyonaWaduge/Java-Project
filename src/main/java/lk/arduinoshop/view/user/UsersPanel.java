    private void loadUsersAndPrivileges() {

        allUsers = UserController.allUsers();

        // ---- Set data to user list table
        DefaultTableModel tableModel = (DefaultTableModel) tableUserNameList.getModel();
        tableModel.setRowCount(0);

        for (UserDTO userDTO : allUsers) {
            Object[] row = new Object[3];
            row[1] = userDTO.getUserName();
            row[2] = userDTO.getUserStatus();
            row[0] = userDTO.getUserId();
            tableModel.addRow(row);
        }

    }

    private void loadAllPrivileges() {
        List<UserPrivilegeDTO> userPrivilegeDTOList = UserController.allPrivileges();

        DefaultListModel defaultListModel = new DefaultListModel();

        for (UserPrivilegeDTO userPrivilegeDTO : userPrivilegeDTOList) {
            defaultListModel.addElement(userPrivilegeDTO.getPrivilege());
        }

        allPrivilegesList.setModel(defaultListModel);

    }

    private void clearPanelData() {

        useNameTextField.setText("");
        passwordField.setText("");
        rePasswordField.setText("");
        loadAllPrivileges();
    }

