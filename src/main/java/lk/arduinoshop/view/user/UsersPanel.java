        if( selectedRow == -1) {
            return;
        }
        
        int userId = Integer.parseInt(defaultTableModel.getValueAt(selectedRow, 0).toString());
        String status = defaultTableModel.getValueAt(selectedRow, 2).toString();
        
         if( status.equals("NEW") || status.equals("NEW") ) {
            return;
        }
        
        String password = JOptionPane.showInputDialog("Please Enter Default Password to re-set Password ");
        
        UserDTO userDTO = new UserDTO();
